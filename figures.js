export const getHeart = (scale, angle, xOffset, yOffset, rot) =>
  [
    [
      { x: 6.03, y: 4.62 },
      { x: 6.1, y: 4.39 },
      { x: 6.63, y: 4.34 },
      { x: 6.47, y: 4.61 },
    ],
    [
      { x: 6.03, y: 4.62 },
      { x: 6.19, y: 4.98 },
      { x: 6.64, y: 4.89 },
      { x: 6.47, y: 4.61 },
    ],
  ]
    .map((l) => {
      const [p0, p1, p2, p3] = l;
      return [
        { x: p0.x * scale, y: p0.y * scale },
        { x: p1.x * scale, y: p1.y * scale },
        { x: p2.x * scale, y: p2.y * scale },
        { x: p3.x * scale, y: p3.y * scale },
      ];
    })
    .map((l) => {
      const [p0, p1, p2, p3] = l;
      return [
        rot(p0, { rot: { x: 6.25, y: 4.64, angle } }),
        rot(p1, { rot: { x: 6.25, y: 4.64, angle } }),
        rot(p2, { rot: { x: 6.25, y: 4.64, angle } }),
        rot(p3, { rot: { x: 6.25, y: 4.64, angle } }),
      ];
    })
    .map((l) => {
      const [p0, p1, p2, p3] = l;
      return [
        { x: p0.x + xOffset, y: p0.y + yOffset },
        { x: p1.x + xOffset, y: p1.y + yOffset },
        { x: p2.x + xOffset, y: p2.y + yOffset },
        { x: p3.x + xOffset, y: p3.y + yOffset },
      ];
    });

